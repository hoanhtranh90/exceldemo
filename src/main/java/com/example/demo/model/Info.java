package com.example.demo.model;

public class Info {
    private String maSv;
    private String hoTen;
    private String className;
    private String diemThi;
    private String diemTongKet;
    private String hocPhan;



    public Info() {
    }

    public String getMaSv() {
        return maSv;
    }

    public void setMaSv(String maSv) {
        this.maSv = maSv;
    }

    public String getHoTen() {
        return hoTen;
    }

    public String getHocPhan() {
        return hocPhan;
    }

    public void setHocPhan(String hocPhan) {
        this.hocPhan = hocPhan;
    }

    public String getDiemTongKet() {
        return diemTongKet;
    }

    public void setDiemTongKet(String diemTongKet) {
        this.diemTongKet = diemTongKet;
    }

    public void setHoTen(String hoTen) {
        this.hoTen = hoTen;
    }

    public String getClassName() {
        return className;
    }


    public void setClassName(String className) {
        this.className = className;
    }

    public String getDiemThi() {
        return diemThi;
    }

    public void setDiemThi(String diemThi) {
        this.diemThi = diemThi;
    }
}
